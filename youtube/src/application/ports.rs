//! # ポート（Ports）の概念
//!
//! ポート（ports）はヘキサゴナルアーキテクチャ（Hexagonal Architecture）または
//! ポートとアダプターアーキテクチャ（Ports and Adapters Architecture）に由来しています。
//!
//! ## ポートの役割
//!
//! * **境界の定義**: アプリケーション層と外部（インフラストラクチャなど）との境界を定義する抽象インターフェース
//! * **依存性の逆転**: 内側の層（アプリケーション）が外側の層（インフラストラクチャ）に依存せず、
//!   逆に外側の層が内側の層の定義したインターフェース（ポート）に依存する
//! * **交換可能性**: 様々な実装（アダプター）をポートに対して交換可能にし、テスト容易性と拡張性を高める
//!
//! ポートを介して、アプリケーション層のユースケースは実装の詳細を知らずに外部機能を利用できます。
//! これにより、テスト容易性、保守性、拡張性が向上します。

